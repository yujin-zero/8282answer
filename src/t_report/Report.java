package t_report;

public class Report {
	private int num_r;
	private int num_q;
	private int report_class;
	private int num_attacker;
	private String id_reporter;
	private int keyword;
	public int getNum_r() {
		return num_r;
	}
	public void setNum_r(int num_r) {
		this.num_r = num_r;
	}
	public int getNum_q() {
		return num_q;
	}
	public void setNum_q(int num_q) {
		this.num_q = num_q;
	}
	public int getReport_class() {
		return report_class;
	}
	public void setReport_class(int report_class) {
		this.report_class = report_class;
	}
	public int getNum_attacker() {
		return num_attacker;
	}
	public void setNum_attacker(int num_attacker) {
		this.num_attacker = num_attacker;
	}
	public String getId_reporter() {
		return id_reporter;
	}
	public void setId_reporter(String id_reporter) {
		this.id_reporter = id_reporter;
	}
	public int getKeyword() {
		return keyword;
	}
	public void setKeyword(int keyword) {
		this.keyword = keyword;
	}
	
	
}
