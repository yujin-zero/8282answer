package t_member;

public class User {
	private int num_m;
	private String name;
	private String id;
	private String password;
	private int point;
	private int grade;
	private int report_q;
	private int report_a;
	public int getNum_m() {
		return num_m;
	}
	public void setNum_m(int num_m) {
		this.num_m = num_m;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getReport_q() {
		return report_q;
	}
	public void setReport_q(int report_q) {
		this.report_q = report_q;
	}
	public int getReport_a() {
		return report_a;
	}
	public void setReport_a(int report_a) {
		this.report_a = report_a;
	}
	
	
}
