package t_answer;

public class Answer {

	private int num_a;
	private int num_q;
	private int num_m;
	private String content_a;
	private int recommend;
	private String date_a;
	public int getNum_a() {
		return num_a;
	}
	public void setNum_a(int num_a) {
		this.num_a = num_a;
	}
	public int getNum_q() {
		return num_q;
	}
	public void setNum_q(int num_q) {
		this.num_q = num_q;
	}
	public int getNum_m() {
		return num_m;
	}
	public void setNum_m(int num_m) {
		this.num_m = num_m;
	}
	public String getContent_a() {
		return content_a;
	}
	public void setContent_a(String content_a) {
		this.content_a = content_a;
	}
	public int getRecommend() {
		return recommend;
	}
	public void setRecommend(int recommend) {
		this.recommend = recommend;
	}
	public String getDate_a() {
		return date_a;
	}
	public void setDate_a(String date_a) {
		this.date_a = date_a;
	}
	
	
}
